        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  "DECIMAL(9,2)", "DEFAULT 0 ", "NOT NULL";
      }
      elsif ( $type eq 8 ) {
        $default = 0 unless ( $default );
        $size = 1 unless ( $size );
        #print SQL "  W${name}               NUMBER($size)            DEFAULT 0 NOT NULL\n";
        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  ($dbms eq "oracle" ? "NUMBER($size)" : "INTEGER"), "DEFAULT $default ", "NOT NULL";
      }

      if ( $type eq 5 and $dbms eq "oracle" ) {
        print SQL ") LOB( W${name} ) STORE AS ( TABLESPACE &tclobs )\n";
      }
      else {
        print SQL ")\n" if ( $dbms eq "oracle" );
      }
      print SQL "/\n" if ( $dbms eq "oracle" );
      print SQL "GO\n" if ( $dbms eq "mssql" );
    }
  }
  else {
    #print "Won't " . ${$_}{name} . "\n";
  }
}

