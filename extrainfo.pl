sub gen_core_extra_triggs($;$$) {

  $_ = shift;
  my $table = shift;
  my $objClass = shift;

  if ( ${$_}{name} =~ /Extra(.+)Fields/ ) {
    $objClass = $1 unless ( $objClass );
    #print ${$_}{name} . "\n";

    $table = "W${objClass}_Extra" unless ( $table );
    my $trig  = $table;
    my $base = $objClass;
    $base = "ULM" if ( $objClass eq "Page" );
    $base = "ULM" if ( $objClass eq "Course" );
    $base = "ULM" if ( $objClass eq "Exercise" );
    $base = "Question" if ( $objClass eq "DllQuestion" );
    $base = "Answer" if ( $objClass eq "DllAnswer" );
    $base = "Action" if ( $objClass =~ /Action$/ );

    $base = "OfferingPart" if ( $objClass eq "OfferedClass" );
    $base = "OfferingPart" if ( $objClass eq "OfferedEvent" );
    $base = "OfferingPart" if ( $objClass eq "OfferedResource" );

    if ( $objClass eq "RegistrationRequest_UExtra" or $table eq "WRegistrationRequest_UExtra" ) {
      $table = "WRegistrationRequest_UExtra";
      $trig = "WRegistrationRequest_UExtra";
      $objClass = "RegistrationRequest";
      $base = "RegistrationRequest";
    }
    else {
      $table = "W${base}_Extra";
    }

    #print SQL "--================================================================================\n";
    #print SQL "-- TABLE $table:\n";
    #print SQL "--================================================================================\n";

    if ( $dbms eq "oracle" ) {
      print SQL "CREATE OR REPLACE TRIGGER i$trig\n";
      print SQL "AFTER INSERT OR DELETE ON W${objClass}\n";
      print SQL "FOR EACH ROW\n";
      print SQL "BEGIN\n";
      print SQL "  IF ( INSERTING ) THEN\n";
      print SQL "    INSERT INTO $table (W${base}_ID) VALUES (:new.W${objClass}_ID);\n";
      print SQL "  END IF;\n";
      print SQL "  IF ( DELETING ) THEN\n";
      print SQL "    DELETE FROM $table WHERE W${base}_ID  = :old.W${objClass}_ID;\n";
      print SQL "  END IF;\n";
      print SQL "END;\n";
      print SQL "/\n";
      print SQL "SHOW ERRORS\n";
      print SQL "\n";
    }
    else {
      print SQL "\n";
      print SQL "--================================================================================\n";
      print SQL "-- Trigger: i$trig\n";
      print SQL "--================================================================================\n";
      print SQL "IF EXISTS\n";
      print SQL " (SELECT NULL FROM sysobjects\n";
      print SQL "  WHERE name = 'i$trig' AND Type = 'TR' )\n";
      print SQL "BEGIN\n";
      print SQL "  DROP TRIGGER i$trig;\n";
      print SQL "  PRINT 'INSTALL --> TRIGGER i$trig dropped';\n";
      print SQL "END\n";
      print SQL "GO\n";
      print SQL "CREATE TRIGGER i$trig\n";
      print SQL "ON W${objClass}\n";
      print SQL "  AFTER INSERT, DELETE\n";
      print SQL "AS\n";
      print SQL "BEGIN\n";
      print SQL "  INSERT INTO $table (W${base}_ID)\n";
      print SQL "  SELECT W${objClass}_ID FROM Inserted;\n";
      print SQL "  DELETE FROM $table WHERE W${base}_ID IN\n";
      print SQL "  (SELECT W${objClass}_ID FROM Deleted);\n";
      print SQL "END;\n";
      print SQL "\n";
      print SQL "GO\n";
      print SQL "IF( \@\@ERROR <> 0 ) BEGIN\n";
      print SQL "  PRINT      'ERROR!  --> Failed to create TRIGGER i$trig';\n";
      print SQL "  RAISERROR ('ERROR!  --> Failed to create TRIGGER i$trig', 16, 127);\n";
      print SQL "END ELSE\n";
      print SQL "  PRINT 'INSTALL --> TRIGGER i$trig created';\n";
      print SQL "GO\n";
      print SQL "\n";
    }
  }
  else {
    #print "Won't " . ${$_}{name} . "\n";
  }
}

