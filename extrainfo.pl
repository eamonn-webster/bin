      printf SQL $fmt, "l${objClass}_ID", "INTEGER";
      foreach ( @{${$_}{option}} ) {
        my $name = ucfirst ${$_}{name};
        my $size = ${$_}{Size};
        my $type = ${$_}{Type};
        if ( ${$_}{value} ) {
          $size = ${$_}{value}->{size};
          $type = ${$_}{value}->{type};
