sub gen_core_extra_fields($;$) {

  $_ = shift;
  my $table = shift;

  if ( ${$_}{name} =~ /Extra(.+)Fields/ ) {
    my $objClass = $1;
    #print ${$_}{name} . "\n";

    $table = "W${objClass}_Extra" unless ( $table );
    print SQL "--================================================================================\n";
    print SQL "-- TABLE $table:\n";
    print SQL "--================================================================================\n";

    #print ${$_}{option} . "\n";
    foreach ( @{${$_}{option}} ) {
      my $name = ucfirst ${$_}{name};
      $name =~ s!_id$!_ID!i;
      my $size = ${$_}{Size};
      my $type = ${$_}{Type};
      my $default = ${$_}{Default};
      my $nullable = ${$_}{Nullable};
      $default = "$default";
      if ( $default =~ /HASH\(0x.+\)/ ) {
        $default = "";
      }
      else {
        #print "name: $name default: ". keys ( %{$default} ) ."\n";
        #print "name: $name default: $default\n";
      }
      #print "name: $name type: $type size: $size default: $default\n";
      if ( ${$_}{value} ) {
        $size = ${$_}{value}->{size};
        $type = ${$_}{value}->{type};
        $default = ${$_}{value}->{default};
        $nullable = ${$_}{value}->{nullable};
        $default = "$default";
        if ( $default =~ /HASH\(0x.+\)/ ) {
          $default = "";
        }
      }
      print SQL "ALTER TABLE $table ADD\n";
      print SQL "(\n" if ( $dbms eq "oracle" );

      #my ($sqlname, $sqltype, $sqldef, $sqlnull);

      my $sqlnull;
      my $sqltype;
      if ( $type eq 1 ) {
        unless ( $size ) {
          $size = 255 if ( $dbms eq "oracle" );
          $size = 254 if ( $dbms eq "mssql" );
        }
        unless ( $default ) {
          $default = "''" if ( $dbms eq "oracle" );
          $default = "N''" if ( $dbms eq "mssql" );
        }

        if ( $dbms eq "oracle" ) {
          $sqlnull = " NULL" ;
        }
        else {
          $sqlnull = "";
        }
        if ( $dbms eq "oracle" ) {
          $sqltype = "VARCHAR2" ;
        }
        else {
          $sqltype = "NVARCHAR";
        }
        #print SQL "  W${name}               VARCHAR2($size)         DEFAULT $default NULL\n";
        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  "$sqltype($size)", "DEFAULT $default", $sqlnull;
      }
      elsif ( $type eq 2 ) {
        $size = 10 unless ( $size );
        if ( $nullable ) {
          #$default = 0 unless ( $default );
          #print SQL "  W${name}               NUMBER($size)         NULL\n";
          printf SQL "  %-21s %-21s%s%s\n", "W${name}",  ($dbms eq "oracle" ? "NUMBER($size)" : "INTEGER"), "NULL";
        }
        else {
          $default = 0 unless ( $default );
          #print SQL "  W${name}               NUMBER($size)         DEFAULT $default NOT NULL\n";
          printf SQL "  %-21s %-21s%s%s\n", "W${name}",  ($dbms eq "oracle" ? "NUMBER($size)" : "INTEGER"), "DEFAULT $default ", "NOT NULL";
        }
      }
      elsif ( $type eq 3 ) {
        #print SQL "  W${name}               DATE         NULL\n";
        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  dateType(), "NULL";
      }
      elsif ( $type eq 5 ) {
        #print SQL "  W${name}               CLOB         DEFAULT EMPTY_CLOB()\n";
        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  ($dbms eq "oracle" ? "CLOB" : "NTEXT"), ($dbms eq "oracle" ? "DEFAULT EMPTY_CLOB()" : "DEFAULT N''");
      }
      elsif ( $type eq 6 ) {
        if ( $Major ge 9 ) {
          $size = 19 unless ( $size );
        }
        else {
          $size = 10 unless ( $size );
        }
        #print SQL "  W${name}               NUMBER($size)         NULL\n";
        printf SQL "  %-21s %-21s%s%s\n", "W${name}",  ($dbms eq "oracle" ? "NUMBER($size)" : ($Major ge 9 ? "NUMERIC($size)" : "INTEGER")), "", "NULL";
      }
