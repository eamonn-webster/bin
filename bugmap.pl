sub map_ids($$$) {
    my ($line, $changed, $found80000) = @_;
    foreach ( $line =~ /#([0-9]{4,5})[^0-9]/g ) {
        if ( $1 eq "80000" ) {
            $$found80000 = 1;
        }
        my $old_id = $_;
        my $new_id = $bug_map{$old_id};
        if ( $new_id ne "" ) {
            $$changed = 1;
            $line =~ s/#$old_id/#$new_id/g;
        }
    }
    return $line;
}


sub updatefile($) {
    my ($file) = @_;
    if ( open( IN, $file ) ) {
        my $changed = 0;
        my $found80000 = 0;
        if ( open( OUT, ">$file.new" ) ) {
            while ( <IN> ) {
                $_ = map_ids( $_, \$changed, \$found80000 );
                print OUT;
            }
            close( OUT );
        }
        close( IN );
        if ( $found80000 ) {
            print "found 80000\n";
        }
        if ( $changed == 0 ) {
            unlink( "$file.new" );
        }
        else {
            if ( -e "$file.old" ) {
                unlink( "$file.old" );
            }
            rename( $file, "$file.old" );
            rename( "$file.new", $file );
        }
    }
}

